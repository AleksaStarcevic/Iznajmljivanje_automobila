    public void popuniTabelu() {

        tblKorisnici.setModel(new javax.swing.table.DefaultTableModel(
                new Object[][]{},
                new String[]{
                    "Korisnicko ime"}
        ));

        ArrayList<Korisnik> korisnici =(ArrayList<Korisnik>) ServerThread.getPrijavljeniKorisnici();
        DefaultTableModel dtm = (DefaultTableModel) tblKorisnici.getModel();
        for (Korisnik korisnik : korisnici) {
            String[] row = {korisnik.getKorisnickoIme()};
            dtm.addRow(row);
        }

    }
    
    private void popuniFormu() {
        txtUrl.setText(PodesavanjaBaze.getInstance().getProperty("url"));
        txtUsername.setText(PodesavanjaBaze.getInstance().getProperty("username"));
        txtPassword.setText(PodesavanjaBaze.getInstance().getProperty("password"));
        txtIP.setText(PodesavanjaKonekcije.getInstance().getProperty("adresa"));
        txtPort.setText(PodesavanjaKonekcije.getInstance().getProperty("port"));
    }
    
    private void onemoguciPolja() {
        btnBaza.setEnabled(false);
        btnKonekcija.setEnabled(false);
        txtUrl.setEnabled(false);
        txtUsername.setEnabled(false);
        txtPassword.setEnabled(false);
        txtPort.setEnabled(false);
        txtIP.setEnabled(false);
    }

