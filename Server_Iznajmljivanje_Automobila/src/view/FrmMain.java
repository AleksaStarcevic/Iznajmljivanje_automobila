    private void btnBazaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBazaActionPerformed
       String url = txtUrl.getText();
        String user = txtUsername.getText();
        String password = txtPassword.getText();
        PodesavanjaBaze.getInstance().setProperty("url", url);
        PodesavanjaBaze.getInstance().setProperty("username", user);
        PodesavanjaBaze.getInstance().setProperty("password", password);
        try {
            PodesavanjaBaze.getInstance().ucitaj();
            JOptionPane.showMessageDialog(this, "Uspesno podesena baza!");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Greska! Neuspesno podesena baza!");
        }
    }//GEN-LAST:event_btnBazaActionPerformed

