    private void prepareTable() throws SQLException {
        List<PotvrdaOIznajmljivanju> potvrde = new ArrayList<>();
        TableModelPotvrde model = new TableModelPotvrde(potvrde);
        tblPotvrde.setModel(model);
       
        List<Automobil> automobili = Kontroler.getInstanca().getStorageAutomobili();
        JComboBox cbAutomobili = new JComboBox(automobili.toArray());
        TableColumn tcAutomobil = tblPotvrde.getColumnModel().getColumn(1);
        tcAutomobil.setCellEditor(new DefaultCellEditor(cbAutomobili));
        
        List<Vozac> vozaci = Kontroler.getInstanca().getStorageVozac();
        JComboBox cbVozaci = new JComboBox(vozaci.toArray());
        TableColumn tcVozaci = tblPotvrde.getColumnModel().getColumn(2);
        tcVozaci.setCellEditor(new DefaultCellEditor(cbVozaci));
    }
