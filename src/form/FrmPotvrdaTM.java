    private void brnObrisiPotvrduActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_brnObrisiPotvrduActionPerformed
       int red = tblPotvrde.getSelectedRow();
        TableModelPotvrde tbp = (TableModelPotvrde) tblPotvrde.getModel();
        if(red != -1){
            tbp.obrisiIzTabele(red);
        }else{
            JOptionPane.showMessageDialog(this, "Nije selektovan red!");
        }
    }//GEN-LAST:event_brnObrisiPotvrduActionPerformed

